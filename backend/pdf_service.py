from reportlab.lib.pagesizes import letter
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle
from reportlab.lib.styles import getSampleStyleSheet
from reportlab.lib import colors
import io

def generate_medical_report(report_data: dict) -> io.BytesIO:
    """
    Generates a professional PDF medical report.
    Returns an in-memory BytesIO object.
    """
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(buffer, pagesize=letter, rightMargin=40, leftMargin=40, topMargin=40, bottomMargin=40)
    
    styles = getSampleStyleSheet()
    title_style = styles['Heading1']
    subtitle_style = styles['Heading2']
    normal_style = styles['Normal']
    
    story = []
    
    # Report Header
    disease_name = report_data.get('disease', 'Medical')
    story.append(Paragraph(f"MED-CORE OS: {disease_name} Assessment Report", title_style))
    story.append(Spacer(1, 15))
    
    # 1. Risk Summary
    story.append(Paragraph("Risk Assessment Overview", subtitle_style))
    story.append(Spacer(1, 6))
    
    risk_category = report_data.get('risk_category', 'Unknown')
    risk_pct = report_data.get('risk_percentage', 0.0)
    story.append(Paragraph(f"<b>Risk Category:</b> {risk_category}", normal_style))
    story.append(Paragraph(f"<b>Probability Score:</b> {risk_pct}%", normal_style))
    story.append(Spacer(1, 15))
    
    # 2. Explainable AI (SHAP Logic)
    factors = report_data.get('important_factors', [])
    if factors:
        story.append(Paragraph("AI Logic Explanation (Top Driving Factors)", subtitle_style))
        story.append(Spacer(1, 6))
        factor_data = [["Biological Feature", "AI Impact Direction", "Contribution Score"]]
        for f in factors:
            factor_data.append([f.get("feature", "N/A"), f.get("impact", "N/A"), str(f.get("score", "N/A"))])
            
        t = Table(factor_data, colWidths=[200, 150, 120])
        t.setStyle(TableStyle([
            ('BACKGROUND', (0,0), (-1,0), colors.HexColor("#12161e")),
            ('TEXTCOLOR', (0,0), (-1,0), colors.whitesmoke),
            ('ALIGN', (0,0), (-1,-1), 'CENTER'),
            ('FONTNAME', (0,0), (-1,0), 'Helvetica-Bold'),
            ('BOTTOMPADDING', (0,0), (-1,0), 10),
            ('BACKGROUND', (0,1), (-1,-1), colors.HexColor("#f8fafc")),
            ('GRID', (0,0), (-1,-1), 1, colors.black)
        ]))
        story.append(t)
        story.append(Spacer(1, 15))
        
    # 3. Patient Vitals (Input Data)
    vitals = report_data.get('vitals', {})
    if vitals:
        story.append(Paragraph("Patient Input Vitals", subtitle_style))
        story.append(Spacer(1, 6))
        vitals_data = [["Vital Sign", "Recorded Value"]]
        for k, v in vitals.items():
            # Clean up the key names for display
            nice_key = k.replace("_", " ").title()
            vitals_data.append([nice_key, str(v)])
            
        vt = Table(vitals_data, colWidths=[250, 220])
        vt.setStyle(TableStyle([
            ('BACKGROUND', (0,0), (-1,0), colors.HexColor("#12161e")),
            ('TEXTCOLOR', (0,0), (-1,0), colors.whitesmoke),
            ('ALIGN', (0,0), (-1,-1), 'CENTER'),
            ('FONTNAME', (0,0), (-1,0), 'Helvetica-Bold'),
            ('BOTTOMPADDING', (0,0), (-1,0), 10),
            ('GRID', (0,0), (-1,-1), 1, colors.black)
        ]))
        story.append(vt)
        story.append(Spacer(1, 15))
        
    # 4. Medical Disclaimer / Advice
    story.append(Paragraph("Medical Disclaimer", subtitle_style))
    story.append(Spacer(1, 6))
    advice = report_data.get('advice', "Please consult a doctor.")
    story.append(Paragraph(f"<i>{advice}</i>", normal_style))
    story.append(Spacer(1, 6))
    story.append(Paragraph("<i>Note: This report is generated by MED-CORE AI and is for informational purposes only. It is not a substitute for professional medical diagnosis.</i>", normal_style))
    
    # Generate PDF
    doc.build(story)
    buffer.seek(0)
    return buffer
